const CHAR_W: usize = 8;
const CHAR_H: usize = 8;
const FG_COLOR: u32 = 0x00CCCCCC; // 明るいグレー
const BG_COLOR: u32 = 0x00000000; // 黒

/// ブートフェーズ専用 VGA フレームバッファコンソール
pub struct VgaConsole {
    fb:     *mut u32,
    width:  usize,
    height: usize,
    stride: usize, // ピクセル単位の1行あたりの幅
    col:    usize, // 現在の文字カラム
    row:    usize, // 現在の文字ロウ
}

// ブートローダーはシングルスレッド動作なので Send/Sync を手動実装
unsafe impl Send for VgaConsole {}
unsafe impl Sync for VgaConsole {}

impl VgaConsole {
    pub const fn new() -> Self {
        Self { fb: core::ptr::null_mut(), width: 0, height: 0, stride: 0, col: 0, row: 0 }
    }

    /// GOP フレームバッファで初期化し、画面をクリア
    pub fn init(&mut self, fb_ptr: *mut u32, width: usize, height: usize, stride: usize) {
        self.fb = fb_ptr;
        self.width = width;
        self.height = height;
        self.stride = stride;
        self.col = 0;
        self.row = 0;
        self.clear();
    }

    pub fn clear(&mut self) {
        if self.fb.is_null() { return; }
        let total = self.stride * self.height;
        unsafe {
            for i in 0..total {
                self.fb.add(i).write_volatile(BG_COLOR);
            }
        }
        self.col = 0;
        self.row = 0;
    }

    fn cols(&self) -> usize { self.width  / CHAR_W }
    fn rows(&self) -> usize { self.height / CHAR_H }

    /// 1行上にスクロール
    fn scroll_up(&mut self) {
        if self.fb.is_null() { return; }
        let row_pixels = CHAR_H * self.stride;
        let total_rows = self.rows();
        // 全行を1行分上に移動
        unsafe {
            let src = self.fb.add(row_pixels);
            let len = row_pixels * (total_rows - 1);
            core::ptr::copy(src, self.fb, len);
            // 最下行をクリア
            let last_row = self.fb.add(row_pixels * (total_rows - 1));
            for i in 0..row_pixels {
                last_row.add(i).write_volatile(BG_COLOR);
            }
        }
        self.row = total_rows - 1;
    }

    /// 文字を現在位置に描画（制御文字処理あり）
    pub fn put_char(&mut self, c: char) {
        if self.fb.is_null() { return; }

        match c {
            '\n' => {
                self.col = 0;
                self.row += 1;
                if self.row >= self.rows() {
                    self.scroll_up();
                }
                return;
            }
            '\r' => {
                self.col = 0;
                return;
            }
            _ => {}
        }

        let code = c as usize;
        let glyph = if code >= 0x20 && code <= 0x7F {
            &FONT_8X8[code - 0x20]
        } else {
            &FONT_8X8[0] // 不明文字はスペース
        };

        let px = self.col * CHAR_W;
        let py = self.row * CHAR_H;

        for (row_idx, &bits) in glyph.iter().enumerate() {
            for bit_idx in 0..CHAR_W {
                let color = if (bits >> bit_idx) & 1 != 0 { FG_COLOR } else { BG_COLOR };
                let x = px + bit_idx;
                let y = py + row_idx;
                if x < self.width && y < self.height {
                    unsafe {
                        self.fb.add(y * self.stride + x).write_volatile(color);
                    }
                }
            }
        }

        self.col += 1;
        if self.col >= self.cols() {
            self.col = 0;
            self.row += 1;
            if self.row >= self.rows() {
                self.scroll_up();
            }
        }
    }
}

impl fmt::Write for VgaConsole {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        for c in s.chars() {
            self.put_char(c);
        }
        Ok(())
    }
}

/// グローバルコンソール（シングルスレッドのブートローダー用）
pub static mut CONSOLE: VgaConsole = VgaConsole::new();
