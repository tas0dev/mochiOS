/// フレームバッファライター
pub struct Writer {
    col: usize,
    row: usize,
    max_cols: usize,
    max_rows: usize,
}

impl Writer {
    fn new(info: &FramebufferInfo) -> Self {
        Self {
            col: 0,
            row: 0,
            max_cols: info.width  / FONT_WIDTH,
            max_rows: info.height / FONT_HEIGHT,
        }
    }

    fn put_pixel(info: &FramebufferInfo, x: usize, y: usize, color: u32) {
        if x < info.width && y < info.height {
            let fb_ptr = info.addr as *mut u32;
            unsafe { fb_ptr.add(y * info.stride + x).write_volatile(color); }
        }
    }

    /// ビットマップフォントで1文字を描画
    fn draw_char(&self, ch: u8, x: usize, y: usize, fg: u32, bg: u32) {
        let code = ch as usize;
        let glyph = if code >= 0x20 && code <= 0x7F {
            &FONT_8X8[code - 0x20]
        } else {
            &FONT_8X8[0] // 不明文字はスペース
        };
        if let Some(info) = FB_INFO.get() {
            for (row_idx, &bits) in glyph.iter().enumerate() {
                for bit_idx in 0..FONT_WIDTH {
                    let color = if (bits >> bit_idx) & 1 != 0 { fg } else { bg };
                    Self::put_pixel(info, x + bit_idx, y + row_idx, color);
                }
            }
        }
    }

    /// 1バイト書き込み
    pub fn write_byte(&mut self, byte: u8) {
        match byte {
            b'\n' => self.new_line(),
            byte => {
                if self.col >= self.max_cols {
                    self.new_line();
                }
                let x = self.col * FONT_WIDTH;
                let y = self.row * FONT_HEIGHT;
                self.draw_char(byte, x, y, 0xCCCCCC, 0x000000);
                self.col += 1;
            }
        }
    }

    /// 文字列を書き込み
    pub fn write_string(&mut self, s: &str) {
        for byte in s.bytes() {
            match byte {
                0x20..=0x7E | b'\n' => self.write_byte(byte),
                _ => self.write_byte(b'?'),
            }
        }
    }

    /// 改行処理（スクロール付き）
    fn new_line(&mut self) {
        self.col = 0;
        self.row += 1;
        if self.row >= self.max_rows {
            self.scroll_up();
        }
    }

    /// 1行スクロールアップ
    fn scroll_up(&mut self) {
        if let Some(info) = FB_INFO.get() {
            let fb_ptr = info.addr as *mut u32;
            let row_pixels = FONT_HEIGHT * info.stride;
            let total_rows = self.max_rows;
            unsafe {
                let src = fb_ptr.add(row_pixels);
                let len = row_pixels * (total_rows - 1);
                core::ptr::copy(src, fb_ptr, len);
                // 最終行をクリア
                let last = fb_ptr.add(row_pixels * (total_rows - 1));
                for i in 0..(row_pixels) {
                    last.add(i).write_volatile(0x000000);
                }
            }
            self.row = total_rows - 1;
        }
    }

    /// 画面全体をクリア
    pub fn clear_screen(&mut self) {
        if let Some(info) = FB_INFO.get() {
            let fb_ptr = info.addr as *mut u32;
            let total = info.height * info.stride;
            unsafe {
                for i in 0..total {
                    fb_ptr.add(i).write_volatile(0x000000);
                }
            }
        }
        self.row = 0;
        self.col = 0;
    }
}

impl Write for Writer {
    fn write_str(&mut self, s: &str) -> fmt::Result {
        self.write_string(s);
        Ok(())
    }
}

/// グローバルライター
static WRITER: Once<Mutex<Writer>> = Once::new();

/// フレームバッファ情報を返す（syscall 用）
pub fn get_info() -> Option<FramebufferInfo> {
    FB_INFO.get().copied()
}

/// フレームバッファを初期化
pub fn init(addr: u64, width: usize, height: usize, stride: usize) {
    FB_INFO.call_once(|| FramebufferInfo { addr, width, height, stride });
    if let Some(info) = FB_INFO.get() {
        WRITER.call_once(|| Mutex::new(Writer::new(info)));
        if let Some(writer) = WRITER.get() {
            writer.lock().clear_screen();
        }
    }
}

/// フレームバッファに文字列を出力
pub fn print(args: fmt::Arguments) {
    if let Some(writer) = WRITER.get() {
        x86_64::instructions::interrupts::without_interrupts(|| {
            let _ = writer.lock().write_fmt(args);
        });
    }
}

/// カーソルのピクセルY座標を設定する（シェルとの同期用）
pub fn set_cursor_pixel_y(pixel_y: usize) {
    if let Some(writer) = WRITER.get() {
        let mut w = writer.lock();
        w.row = pixel_y / FONT_HEIGHT;
        w.col = 0;
    }
}

/// カーソルの現在ピクセルY座標を取得する（シェルとの同期用）
pub fn get_cursor_pixel_y() -> usize {
    if let Some(writer) = WRITER.get() {
        let w = writer.lock();
        w.row * FONT_HEIGHT
    } else {
        0
    }
}

